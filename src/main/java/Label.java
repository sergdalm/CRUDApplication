public class Label {
    private java.awt.Label label;
    private int id;
    private String name;

    public java.awt.Label getLabel() {
        return label;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setLabel(java.awt.Label label) {
        this.label = label;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
